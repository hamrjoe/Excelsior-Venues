package com.techelevator.classes;

public class Venue {

    private long venue_id;
    private String name;
    private long city_id;
    private String description;

    public long getVenue_id() {
        return venue_id;
    }

    public String getName() {
        return name;
    }

    public long getCity_id() {
        return city_id;
    }

    public String getDescription() {
        return description;
    }


}
